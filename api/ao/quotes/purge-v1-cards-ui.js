/**
 * GET /api/ao/quotes/purge-v1-cards-ui
 *
 * Returns a simple HTML page with a button that runs the purge.
 * No terminal needed. Just open the URL while logged in and click the button.
 *
 * Drop this file in: /api/ao/quotes/purge-v1-cards-ui.js
 * Remove it after the purge is confirmed.
 */

import { requireAoSession } from '../../../lib/ao/requireAoSession.js';
import { supabaseAdmin } from '../../../lib/supabase-admin.js';

export default async function handler(req, res) {
  const auth = requireAoSession(req, res);
  if (!auth) return;

  // Handle the actual delete (POST from the button)
  if (req.method === 'POST') {
    try {
      const { count, error: countErr } = await supabaseAdmin
        .from('ao_scheduled_posts')
        .select('*', { count: 'exact', head: true })
        .eq('status', 'scheduled')
        .eq('source_kind', 'auto_pull_quote_card')
        .eq('intent->>created_by_email', auth.email);

      if (countErr) {
        res.setHeader('Content-Type', 'text/html');
        return res.status(500).send(`<p style="color:red;font-family:sans-serif;padding:2rem;">Error counting rows: ${countErr.message}</p>`);
      }

      const { error: deleteErr } = await supabaseAdmin
        .from('ao_scheduled_posts')
        .delete()
        .eq('status', 'scheduled')
        .eq('source_kind', 'auto_pull_quote_card')
        .eq('intent->>created_by_email', auth.email);

      if (deleteErr) {
        res.setHeader('Content-Type', 'text/html');
        return res.status(500).send(`<p style="color:red;font-family:sans-serif;padding:2rem;">Error deleting rows: ${deleteErr.message}</p>`);
      }

      res.setHeader('Content-Type', 'text/html');
      return res.status(200).send(`
        <!DOCTYPE html>
        <html>
        <head><title>Purge Complete</title></head>
        <body style="font-family:sans-serif;max-width:480px;margin:4rem auto;padding:0 1rem;">
          <h1 style="color:#16a34a;">Done.</h1>
          <p style="font-size:1.1rem;">Deleted <strong>${count || 0} rows</strong> from ao_scheduled_posts.</p>
          <p style="color:#6b7280;">V1 quote cards have been removed. No content published to your channels.</p>
          <p style="margin-top:2rem;"><a href="/ao/analyst" style="color:#2563eb;">Back to Auto →</a></p>
        </body>
        </html>
      `);
    } catch (err) {
      res.setHeader('Content-Type', 'text/html');
      return res.status(500).send(`<p style="color:red;font-family:sans-serif;padding:2rem;">Server error: ${err?.message}</p>`);
    }
  }

  // GET — show the confirmation page with the button
  if (req.method === 'GET') {
    // Count first so user sees exactly what will be deleted
    let previewCount = 0;
    try {
      const { count } = await supabaseAdmin
        .from('ao_scheduled_posts')
        .select('*', { count: 'exact', head: true })
        .eq('status', 'scheduled')
        .eq('source_kind', 'auto_pull_quote_card')
        .eq('intent->>created_by_email', auth.email);
      previewCount = count || 0;
    } catch (_) {}

    res.setHeader('Content-Type', 'text/html');
    return res.status(200).send(`
      <!DOCTYPE html>
      <html>
      <head><title>Purge V1 Cards</title></head>
      <body style="font-family:sans-serif;max-width:480px;margin:4rem auto;padding:0 1rem;">
        <h1 style="color:#111;">Purge V1 Quote Cards</h1>
        <p style="font-size:1rem;color:#374151;">
          This will permanently delete <strong>${previewCount} scheduled posts</strong>
          generated by the old Auto V1 system.
        </p>
        <p style="font-size:0.9rem;color:#6b7280;">
          These cards have not published yet. Only rows with
          <code>source_kind = auto_pull_quote_card</code> and
          <code>status = scheduled</code> will be deleted.
          Nothing else in your database is touched.
        </p>
        ${previewCount === 0
          ? `<p style="color:#16a34a;font-weight:600;">Nothing to delete — the table is already clean.</p>`
          : `<form method="POST" action="/api/ao/quotes/purge-v1-cards-ui">
              <button
                type="submit"
                style="
                  margin-top:1.5rem;
                  padding:0.75rem 1.5rem;
                  background:#dc2626;
                  color:white;
                  border:none;
                  border-radius:8px;
                  font-size:1rem;
                  font-weight:600;
                  cursor:pointer;
                "
                onclick="return confirm('Delete ${previewCount} rows permanently? This cannot be undone.');"
              >
                Delete ${previewCount} V1 cards
              </button>
            </form>`
        }
        <p style="margin-top:2rem;"><a href="/ao/analyst" style="color:#6b7280;">← Back to Auto</a></p>
      </body>
      </html>
    `);
  }

  return res.status(405).json({ ok: false, error: 'Method not allowed' });
}
